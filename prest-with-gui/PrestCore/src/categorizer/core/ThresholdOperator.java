public enum ThresholdOperator {
	
	GT(">"), LT("<"), GTE(">="), LTE("<="), EQU("="), BTW("<>") ;	
	
    private final String operator;  
    
    ThresholdOperator(String operator) {
        this.operator = operator;
    }

    public String operator()
    {
    	return operator;
    }

}
